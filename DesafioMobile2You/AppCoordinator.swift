import UIKit

class AppCoordinator {
    
    private let window: UIWindow
    
    init(window: UIWindow) {
        self.window = window
    }
    
    func start() {
        let viewController = ViewController.instantiate()
        window.rootViewController = viewController
        window.makeKeyAndVisible()
    }
}
